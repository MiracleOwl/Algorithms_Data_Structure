import java.util.Scanner;

class MapEntry<K extends Comparable<K>, E> {
    // Each MapEntry object is a pair consisting of a key (a Comparable object)
    // and a value (an arbitrary object).
    K key;
    E value;

    public MapEntry(K key, E val) {
        this.key = key;
        this.value = val;
    }

    public String toString() {
        return "<" + key + "," + value + ">";
    }
}
class SLLNode<E> {
    protected E element;
    protected SLLNode<E> succ;

    public SLLNode(E elem, SLLNode<E> succ) {
        this.element = elem;
        this.succ = succ;
    }

    @Override
    public String toString() {
        return element.toString();
    }
}
class CBHT<K extends Comparable<K>, E> {

    // An object of class CBHT is a closed-bucket hash table, containing
    // entries of class MapEntry.
    private SLLNode<MapEntry<K, E>>[] buckets;

    @SuppressWarnings("unchecked")
    public CBHT(int m) {
        // Construct an empty CBHT with m buckets.
        buckets = (SLLNode<MapEntry<K, E>>[]) new SLLNode[m];
    }

    private int hash(K key) {
        // Translate key to an index of the array buckets.
        return Math.abs(key.hashCode()) % buckets.length;
    }

    public SLLNode<MapEntry<K, E>> search(K targetKey) {
        // Find which if any node of this CBHT contains an entry whose key is equal to targetKey.
        // Return a link to that node (or null if there is none).
        int b = hash(targetKey);
        SLLNode<MapEntry<K, E>> currNode = buckets[b];
        while (currNode != null) {
            MapEntry<K, E> currEntry = currNode.element;
            if (currEntry.key.equals(targetKey)) return currNode;
            else currNode = currNode.succ;
        }
        return null;
    }

    public void insert(K key, E val) {
        // Insert the entry <key, val> into this CBHT.
        // If entry with same key exists, overwrite it.
        MapEntry<K, E> newEntry = new MapEntry<>(key, val);
        int b = hash(key);
        SLLNode<MapEntry<K, E>> currNode = buckets[b];
        while (currNode != null) {
            MapEntry<K, E> currEntry = currNode.element;
            if (currEntry.key.equals(key)) {
                // Make newEntry replace the existing entry ...
                currNode.element = newEntry;
                return;
            } else currNode = currNode.succ;
        }
        // Insert newEntry at the front of the SLL in bucket b ...
        buckets[b] = new SLLNode<>(newEntry, buckets[b]);
    }

    public void delete(K key) {
        // Delete the entry (if any) whose key is equal to key from this CBHT.
        int b = hash(key);
        SLLNode<MapEntry<K, E>> predNode = null, currNode = buckets[b];
        while (currNode != null) {
            MapEntry<K, E> currEntry = currNode.element;
            if (currEntry.key.equals(key)) {
                if (predNode == null) buckets[b] = currNode.succ;
                else predNode.succ = currNode.succ;
                return;
            } else {
                predNode = currNode;
                currNode = currNode.succ;
            }
        }
    }

    public String toString() {
        String temp = "";
        for (int i = 0; i < buckets.length; i++) {
            temp += i + ":";
            SLLNode<MapEntry<K, E>> curr = buckets[i];
            while (curr != null) {
                temp += curr.element.toString() + " ";
                curr = curr.succ;
            }
            temp += "\n";
        }
        return temp;
    }
}



class Lecture{
    String date;
    String time;
    String place;
    Integer fee;
    
    public Lecture(String date,String time, String place,Integer fee)
    {
        this.date = date;
        this.time = time;
        this.place = place;
        this.fee = fee;
    }
    public String getTime() { return time;}
    public void setTime(String time) { this.time = time;}
    public String getDate() { return date;}
    public void setDate(String date) { this.date = date;}
    public String getPlace() { return place;}
    public void setPlace(String place) { this.place = place;}
    public Integer getFee() { return fee;}
    public void setFee(Integer fee) { this.fee = fee;}
}


public class Hash_2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        CBHT<String, Lecture> hashtable = new CBHT<String, Lecture>(2*n);
        for(int i=0;i<n;i++)
        {
            Lecture p = new Lecture(sc.next(), sc.next(), sc.next(), sc.nextInt());
            if(hashtable.search(p.getDate())==null)
            {
                hashtable.insert(p.getDate(), p);
            }
            else
            {
                SLLNode<MapEntry<String, Lecture>> best_for_date = hashtable.search(p.getDate());
                Lecture best_lecture_for_date = best_for_date.element.value;
                if(p.getFee() > best_lecture_for_date.getFee())
                {
                    hashtable.insert(p.getDate(), p);

                }
            }
            String date = sc.next();
            SLLNode<MapEntry<String,Lecture>> result = hashtable.search(date);
            if(result!=null)
            {
                Lecture best = result.element.value;
                System.out.println(best.getTime() + " " + best.getPlace() + " " + best.getFee());       
            }
            else
            {
                System.out.println("No offers!");
            }
        }
    }
}
